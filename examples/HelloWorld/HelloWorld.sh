# To run the program, put the code in `HelloWorld.cs` and
# use `dotnet run`.

$ dotnet run
Hello World

# Sometimes we'll want to build our programs into
# binaries. We can do this using `dotnet build`.

$ dotnet build
Microsoft (R) Build Engine version [version]
Copyright (C) Microsoft Corporation. All rights reserved.

  Building...
  HelloWorld -> bin/Debug/net[version]/HelloWorld.dll

# We can then execute the built binary directly.

$ dotnet bin/Debug/net[version]/HelloWorld.dll
Hello World

